  that.mat = {
    rotate_deg: function(){
      let [axis,ang] = $pop_args(2);
      ang = ang * Math.PI/180.0;
      let x = axis[0], y = axis[1], z = axis[2];
      let len = Math.sqrt(x * x + y * y + z * z);
      let out = new Float32Array(16);
      if (len == 0.0) {
        for (let i = 0; i < 16; ++i)
          out[i] = (i % 5 == 0) ? 1 : 0;
        return;
      }
      x /= len;
      y /= len;
      z /= len;
      let c = Math.cos(ang);
      let s = Math.sin(ang);
      let t = 1.0 - c;
      out[0 ] = t*x*x + c;
      out[4 ] = t*x*y + s*z;
      out[8 ] = t*x*z - s*y;
      out[12] = 0.0;
      out[1 ] = t*x*y - s*z;
      out[5 ] = t*y*y + c;
      out[9 ] = t*y*z + s*x;
      out[13] = 0.0;
      out[2 ] = t*x*z + s*y;
      out[6 ] = t*y*z - s*x;
      out[10] = t*z*z + c;
      out[14] = 0.0;
      out[3 ] = 0.0;
      out[7 ] = 0.0;
      out[11] = 0.0;
      out[15] = 1.0;
      return out;
    }
  }
